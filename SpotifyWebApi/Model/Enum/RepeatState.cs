namespace Spotify.Model.Enum
{
    /// <summary>
    /// The <see cref="RepeatState"/>.
    /// </summary>
    public enum RepeatState
    {
        /// <summary>
        /// Will repeat the current track.
        /// </summary>
        Track,

        /// <summary>
        /// Will repeat the current context.
        /// </summary>
        Context,

        /// <summary>
        /// Will turn repeat off.
        /// </summary>
        Off
    }
}
